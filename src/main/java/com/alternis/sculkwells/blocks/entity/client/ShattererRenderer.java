public class ShattererRenderer extends GeoBlockRenderer<ShattererEntity> {
    public ShattererRenderer(BlockEntityRendererProvider.Context context) {
        super(new ShattererModel());

    }

    @Override
    public RenderType getRenderType(ShattererEntity animatable, ResourceLocation texture, @Nullable MultiBufferSource bufferSource, float partialTick) {
        return RenderType.entityTranslucent(getTextureLocation(animatable));
    }




    private int getLightLevel(Level level, BlockPos pos) {
        int bLight = level.getBrightness(LightLayer.BLOCK, pos);
        int sLight = level.getBrightness(LightLayer.SKY, pos);
        return LightTexture.pack(bLight, sLight);
    }


}
